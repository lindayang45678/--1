import com.lakala.module.comm.ObjectInput;

public class OrderInput extends ObjectInput {
	
	private String tAllOrderId;

	private String tOrderId;
	
	private String orderProviderID;
	
	private String tOrderItemsId;

	public String getTAllOrderId() {
		return tAllOrderId;
	}

	public void setTAllOrderId(String tAllOrderId) {
		this.tAllOrderId = tAllOrderId;
	}

	public String getTOrderId() {
		return tOrderId;
	}

	public void setTOrderId(String tOrderId) {
		this.tOrderId = tOrderId;
	}

	public String getOrderProviderID() {
		return orderProviderID;
	}

	public void setOrderProviderID(String orderProviderID) {
		this.orderProviderID = orderProviderID;
	}

	public String getTOrderItemsId() {
		return tOrderItemsId;
	}

	public void setTOrderItemsId(String tOrderItemsId) {
		this.tOrderItemsId = tOrderItemsId;
	}
	
}
