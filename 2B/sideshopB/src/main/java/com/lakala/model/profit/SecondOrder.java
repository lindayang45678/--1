package com.lakala.model.profit;

import java.math.BigDecimal;
import java.util.Date;

public class SecondOrder implements java.io.Serializable {
   
	/**
	 * 二级订单查询数据
	 */
	private static final long serialVersionUID = 4907697909901180064L;
	
		private String goodsName;
		
		private BigDecimal goodsSalePrice;
		
		private Integer goodsCount;
		
		private BigDecimal sum;
		
		private String state;
		
		private Date receiverTime;
		
		private String invalidState;

		public String getGoodsName() {
			return goodsName;
		}

		public void setGoodsName(String goodsName) {
			this.goodsName = goodsName;
		}

		public BigDecimal getGoodsSalePrice() {
			return goodsSalePrice;
		}

		public void setGoodsSalePrice(BigDecimal goodsSalePrice) {
			this.goodsSalePrice = goodsSalePrice;
		}

		public Integer getGoodsCount() {
			return goodsCount;
		}

		public void setGoodsCount(Integer goodsCount) {
			this.goodsCount = goodsCount;
		}

		public BigDecimal getSum() {
			return sum;
		}

		public void setSum(BigDecimal sum) {
			this.sum = sum;
		}

		public String getState() {
			return state;
		}

		public void setState(String state) {
			this.state = state;
		}

		public Date getReceiverTime() {
			return receiverTime;
		}

		public void setReceiverTime(Date receiverTime) {
			this.receiverTime = receiverTime;
		}

		public String getInvalidState() {
			return invalidState;
		}

		public void setInvalidState(String invalidState) {
			this.invalidState = invalidState;
		}

		public static long getSerialversionuid() {
			return serialVersionUID;
		}
	
}
